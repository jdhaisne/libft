#include "libft.h"

size_t	ft_strlenrc(const char *s, char c)
{
	int i;
	int j;

	i = ft_strlen(s);
	j = 0;
	while (i > 0 && s[i] != c)
	{
		i--;
		j++;
	}
	return (j);
}
