#include "libft.h"
#include <stdlib.h>

void	*ft_memalloc(size_t size)
{
	void *ptr;

	ptr = NULL;
	ptr = (void *)malloc(size);
	if (ptr == NULL)
		return (NULL);
	return (ptr);
}
